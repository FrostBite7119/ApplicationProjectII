                        <p dir="ltr">Apa keunikan Kotlin? Salah satunya, Kotlin merupakan sebuah bahasa pemrograman yang bisa ditargetkan untuk berbagai macam platform (<strong>Multiplatform</strong>) dan juga memiliki beberapa paradigma (<strong>Multiparadigm</strong>). Oleh karena itu, kita perlu tahu apa maksud dari Multiplatform dan Multiparadigm tersebut. 
                            &nbsp;Mari kita bahas satu per satu.&nbsp;</p>
                            <p dir="ltr"><br></p><h1 dir="ltr">Multiparadigm</h1>
                            <p dir="ltr">Mungkin sebagian dari kita masih asing dengan istilah multiparadigm di dunia programing. Programming paradigm adalah sebuah cara untuk mengklasifikasikan bahasa pemrograman berdasarkan fitur yang dimilikinya. Paradigma disini berkaitan dengan bagaimana kode dalam sebuah bahasa pemrograman diatur, seperti mengelompokkan kode atau memodifikasinya. Lebih tepatnya, seperti apa struktur kode bisa diterapkan pada bahasa pemrograman tersebut.</p>
                            <p dir="ltr">Terdapat beberapa paradigma yang umum dimiliki oleh sebuah bahasa pemrograman. Ketika kita ingin mengklasifikasikan bahasa pemrograman berdasarkan cara penulisan sintaksnya, object-oriented programming (<strong>OOP</strong>) dan functional programming (<strong>FP</strong>) merupakan 2 (dua) paradigma yang terkenal di kalangan developer. Dari sisi konstruksi, Kotlin sendiri mencakup keduanya. Anda bisa menggunakan gaya OOP maupun FP, bahkan mencampurnya saat menuliskan sintaks Kotlin. Dengan dukungan tinggi untuk fitur-fitur seperti <strong>higher-order function</strong>, <strong>function types</strong>, dan <strong>lambda,&nbsp;</strong>Kotlin adalah pilihan yang tepat untuk Anda mengeksplorasi <em>functional programming</em>. Hampir semua <a href="https://en.wikipedia.org/wiki/General-purpose_language" target="_blank" rel="noreferrer noopener">General Purpose Language</a> 
                                sekarang sudah mendukung multiparadigm. Untuk lebih jelasnya, mari kita bahas satu per satu kedua paradigma tersebut.</p>
                            <p dir="ltr"><br></p><h2 dir="ltr">Object-oriented Programming</h2>
                            <p dir="ltr">Suatu bahasa pemrograman bisa diklasifikasikan ke dalam OOP jika data dan metode yang digunakan untuk memanipulasinya disimpan sebagai satu unit yang bisa disebut dengan objek. OOP memiliki fitur enkapsulasi yang sempurna. Lebih lanjut, satu-satunya cara yang bisa digunakan agar objek atau pengguna lain dapat mengakses suatu data adalah dengan metode objek. Dengan demikian, cara kerja suatu objek dapat diubah tanpa mempengaruhi kode lain yang menggunakan objek tersebut.&nbsp;</p>
                            <p dir="ltr">Kotlin memberikan dukungan yang luas untuk mengembangkan program berorientasi objek. Di dalam OOP kita akan mengenal beberapa konsep seperti <strong>object</strong>, <strong>classes</strong>, <strong>properties</strong>, <strong>constructor</strong>, dll. Dengan OOP kita bisa memecahkan persoalan yang kompleks menjadi set yang lebih kecil dengan membuat objek. Objek tersebut memiliki 2 (dua) karakteristik utama, yaitu <em>state</em> (keadaan) dan <em>behaviour</em> (perilaku).</p>
                            <p dir="ltr">Kita akan membahas mengenai komponen-komponen OOP pada Kotlin secara lebih mendalam pada sub-modul tersendiri. Sebagai pembuka, mari kita perhatikan contoh berikut:</p><ol><li dir="ltr">
                            <p dir="ltr">Sepeda motor adalah sebuah objek. Sepeda motor memiliki 2 (dua) roda yang mana bisa kita sebut sebagai states. Sepeda motor bisa melaju, bisa direm, bisa juga berganti gigi. Nah, ketiga hal tersebut bisa kita sebut sebagai behaviour.</p></li><li dir="ltr">
                            <p dir="ltr">Kuda adalah sebuah objek. Kuda memiliki nama, warna, dan juga jenis yang mana merupakan states dari Kuda. Kuda bisa berlari, makan, dan lain sebagainya. Inilah yang kita sebut sebagai behaviour.</p></li></ol>
                            <p dir="ltr">Sebuah objek seperti sepeda motor juga bisa memiliki objek-objek kecil di dalamnya, misalnya roda, rem, <em>body</em>, mesin dll. Semuanya bisa saling berhubungan dan berinteraksi. Selain itu, OOP juga berdasar pada konsep Message Passing, yakni sebuah tipe komunikasi antara proses dan objek. Itulah gambaran sederhana konsep OOP yang akan kita pelajari.</p>
                            <p dir="ltr"><br></p><h2 dir="ltr">Functional Programming</h2>
                            <p dir="ltr">Ada begitu banyak persepsi mengenai <em>functional programming.&nbsp;</em>Setiap persepsi tergantung siapa yang mengartikannya. Seorang F-sharp programer akan mengartikan FP sebagai apa yang ada pada F-sharp. Demikian halnya Swift programer akan mengartikan FP sebagai apa yang ada pada Swift. Namun kita bisa menyimpulkan bahwa semua bahasa pemrograman yang mendukung <em>first-class citizen</em> termasuk ke dalam FP. <em>First-class citizen</em> sendiri merupakan sebuah entitas yang mendukung semua operasi yang umumnya tersedia untuk entitas lain. Sebagai contoh, sebuah fungsi yang bisa dikirimkan sebagai parameter.&nbsp;</p>
                            <p dir="ltr"><em>Functional programming</em> merupakan paradigma yang sedang <em>hype</em> di kalangan developer saat ini. Paradigma ini memperlakukan komputasi sebagai evaluasi fungsi matematika dan menghindari perubahan keadaan atau <em>state</em> dan data yang bisa berubah. Dalam FP, pemrograman dilakukan dengan <em>expression</em> atau <em>declaration</em>. Sehingga sebuah fungsi menghasilkan nilai yang hanya bergantung pada argumen yang diteruskan ke fungsi tersebut. Menghilangkan efek seperti perubahan state yang tidak tergantung pada fungsi input, dapat membuatnya lebih mudah untuk memahami dan memprediksi <em>behaviour</em> sebuah program. Inilah tujuan utama dari <em>functional programming</em>.</p>
                            <p dir="ltr">Masih banyak developer yang langsung membayangkan OOP ketika mendengar kata “Kotlin”. Padahal seperti yang telah disebutkan sebelumnya, Kotlin merupakan <em>multiparadigm</em> <em>language</em>. Kotlin memiliki beberapa fitur yang memiliki gaya functional programming, walaupun tidak sepenuhnya functional programming. Mengapa demikian? Sesungguhnya Kotlin adalah sebuah bahasa pemrograman berorientasi objek yang memiliki struktur fungsional. Ini artinya Kotlin juga merupakan sebuah functional programming language.</p>
                            <p dir="ltr">Functional programming juga bertujuan untuk mempermudah kita memahami sebuah kode. Ide dibalik FP adalah menambahkan dan mengurangi hal tertentu untuk membuat kode kita sedikit lebih mudah dipahami. Kotlin memiliki banyak fitur menarik yang membuatnya bisa disebut dengan functional programming seperti higher-order function, function types, extension functions, lambda, dll. Kita akan membahas kembali mengenai functional programming pada Kotlin dan fitur-fitur tersebut pada sub-modul tersendiri. Siap lanjut?</p>
